export function drawSurfer(
  ctx: CanvasRenderingContext2D,
  x: number,
  y: number,
  scale: number = 2,
  isJumping: boolean = false
) {
  const sprite = isJumping ? SURFER_JUMPING : SURFER_STANDING;
  const pixelSize = scale;

  ctx.save();
  
  for (let row = 0; row < sprite.length; row++) {
    for (let col = 0; col < sprite[row].length; col++) {
      const colorIndex = parseInt(sprite[row][col]) as keyof typeof SURFER_COLORS;
      const color = SURFER_COLORS[colorIndex];
      
      if (color !== 'transparent') {
        ctx.fillStyle = color;
        ctx.fillRect(
          x + col * pixelSize,
          y + row * pixelSize,
          pixelSize,
          pixelSize
        );
      }
    }
  }
  
  ctx.restore();
}

